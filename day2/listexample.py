# Description: This program is an example of a list in Python
fruits = ["apple", "banana", "cherry", "date"]
print(fruits)  # Output: ['apple', 'banana', 'cherry', 'date']
print(type(fruits))  # Output: <class 'list'>
print(len(fruits))  # Output: 4


print(fruits[0])  # Output: apple


print(fruits[1])  # Output: banana


print(fruits[2])  # Output: cherry


print(fruits[3])  # Output: date


print(fruits[-1])  # Output: date


print(fruits[-2])  # Output: cherry


print(fruits[-3])  # Output: banana


print(fruits[-4])  # Output: apple


print(fruits[0:2])  # Output: ['apple', 'banana']


print(fruits[1:3])  # Output: ['banana', 'cherry']


print(fruits[2:4])  # Output: ['cherry', 'date']


print(fruits[0:4])  # Output: ['apple', 'banana', 'cherry', 'date']


print(fruits[0:4:2])  # Output: ['apple', 'cherry']


print(fruits[0:4:3])  # Output: ['apple', 'date']


print(fruits[0:4:4])  # Output: ['apple']


print(fruits[0:4:5])  # Output: []

print(fruits[0:4:6])  # Output: ['apple']

